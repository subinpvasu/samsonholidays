                    <br> <p>                  
Much of Keralas exotic appeal is centered in these highland areas of the Western Ghats. Ideal places for adventure tourism and trekking,the fresh mountain air, the mist clad hills and a panoramic view make them worthy of a visit.Places like Munnar in Kerala produce an exotic species of flowers that bloom once in twelve years.<br><br>

Hill stations in Kerala offer tourists an opportunity to spend their vacations amidst clean and fresh air of the hills. Some of the more popular hill stations in Kerala are those of Mattupetty, Munnar, Nelliyampathy, Palakkad, Ponmudi, Wayanad, Idduki, Kannur, Kasargod and Kozhikode. Here you can see the milky waterfalls, mighty rocks, lakes, and hill-plantations. The scenic beauty of these Kerala hillstations cannot be defined in words, it is best experienced in person.<br>
