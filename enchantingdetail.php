<b>Day 2 - Cochin to Munnar</b><br> After breakfast, check out from the hotel and proceed to Munnar (160 km / 4. hrs). Munnar, one of the most famous hill station situated on the Western Ghats at a height of 1600 metres above sea level. It is essentially a tea town ï¿½ miles of unending tea plantation can be seen here. Enroute - Cheyyappara, Valara waterfalls and Karadipara View Point. Overnight, stay at Abad Copper Castle.
