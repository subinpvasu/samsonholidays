<b>Day 1 - Cochin</b> <br>Arrival at Cochin Airport and our representative will meet you and transfer to Chullickal and check in at the hotel. visit, St Francis Church, the oldest Church in India, more than 500-year-old Portuguese Houses, Cantilevered Chinese fishing nets, 16th century Synagogue, Jew Town and Mattancherry Palace, built by Portuguese, Palace contains some of India's most beautiful murals. Overnight, stay at Abad Fort.
