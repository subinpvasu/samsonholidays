                    <p> The mountain ranges in Kerala consisting of the highland area of the Western Ghats, exude an exotic charm. Rising to an average height of 1520 m, the tropical forests of the ghats house rich and unique flora and fauna. The high ranges and hill stations are the favourite spots of nature enthusiasts and adventure seekers.Expansive plantations of tea,coffee,rubber and fragrant cardamom and other spices for which Keralais famous for,are cultivated on the slopes of these hill stations.</p><br>   
