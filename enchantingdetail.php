<b>Day 3 - Munnar</b><br> After breakfast sightseeing to Eravikulam National Park also known as Rajamalai, the natural habitat of Niligiri Thar. Niligiri Thar can see very closely here. Later visit a tea factory and tea museum, Mattupetty dam, Echo point and the Kundala Lake. Boating facilities are available at Mattupetty Dam (entry charges extra payment on your own). Overnight, stay at Abad Copper Castle.
